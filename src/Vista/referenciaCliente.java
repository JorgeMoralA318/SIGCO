    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        // TODO add your handling code here:
        int seleccion = tabla.getSelectedRow();
        String cod = tabla.getValueAt(seleccion, 0).toString();
        String ruc_ci = tabla.getValueAt(seleccion, 1).toString();
        String nombre = tabla.getValueAt(seleccion, 2).toString();
        String direccion = tabla.getValueAt(seleccion, 3).toString();
        String telefono = tabla.getValueAt(seleccion, 4).toString();
        String obs = tabla.getValueAt(seleccion, 5).toString();
        String tipo = tabla.getValueAt(seleccion, 6).toString();

        txtIdReferencia.setText(cod);
        txtrucci.setText(ruc_ci);
        txtnombre.setText(nombre);
        txtDireccion.setText(direccion);
        txtTelefono.setText(telefono);
        txtObs.setText(obs);
        switch (tipo) {
            case "P":
                jCheckBox1.setSelected(true);
                break;
            case "C":
                jCheckBox2.setSelected(true);
                break;
        }
    }//GEN-LAST:event_tablaMouseClicked

