    //metodo para validar campos
    private Boolean validar() {
        if (txtrucci.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, llene los campos requeridos");
            txtrucci.requestFocus();
            txtrucci.setBackground(Color.YELLOW);
            return false;
        } else if (txtnombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, llene los campos requeridos");
            txtnombre.requestFocus();
            txtnombre.setBackground(Color.YELLOW);
            return false;

        } else if (txtDireccion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, llene los campos requeridos");
            txtDireccion.requestFocus();
            txtDireccion.setBackground(Color.YELLOW);
            return false;

        } else if (txtTelefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, llene los campos requeridos");
            txtTelefono.requestFocus();
            txtTelefono.setBackground(Color.YELLOW);
            return false;

        } else {
            return true;
        }
    }

    void vaciar() {
        txtIdReferencia.setText("");
        txtrucci.setText("");
        txtnombre.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        txtObs.setText("");
        txtrucci.requestFocus();

    }

    void eliminar() {
        if (txtIdReferencia.getText().length() > 0) {
            int codigo = Integer.parseInt(txtIdReferencia.getText());
            vo.setCodigo(codigo);
            dao.Eliminar_ReferenciaVO(vo);
            mostrar(txtCliente.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Nada que eliminar");
            txtCliente.requestFocus();
        }

    }

